FGameplayTag GetPLInputNumberedTag(int32 Index)
{
	if (Index < 0 || Index > 9)
	{
		return FGameplayTag();
	}

	const FString TagString = FString::Printf(TEXT("Input.%d"), Index);
	return UGameplayTagsManager::Get().RequestGameplayTag(FName(*TagString), false);
}
